import random

def jogar():

    imprime_mensagens_abertura()

    palavra_secreta = inicializa_palavra_secreta()

    letras_acertadas = inicializa_letras_acertadas(palavra_secreta)

    enforcou = False
    acertou = False
    erros = 0

    while(not enforcou and not acertou):
        chute = pede_chute()

        if(chute in palavra_secreta):
            marca_chute_correto(chute, palavra_secreta, letras_acertadas)
        else:
            erros += 1
            desenha_forca(erros)

        enforcou = erros == 7
        acertou = "_" not in letras_acertadas
        print(letras_acertadas)

    if(acertou):
        imprime_mensagen_vencedor()
    else:
        imprime_mensagen_perdedor(palavra_secreta)

def imprime_mensagens_abertura():
    print("***************************************")
    print("******Bem Vindo ao jogo da Forca!******")
    print("***************************************")

def inicializa_palavra_secreta():
    arquivo = open("Python-parte-1\\arquivo.txt", "r")
    palavras = [linha.strip() for linha in arquivo]
    arquivo.close()
    return palavras[random.randrange(0,len(palavras))].upper()

def inicializa_letras_acertadas(palavra):    
    letras_acertadas = [ '_' for letra in palavra]
    print(letras_acertadas)
    return letras_acertadas

def pede_chute():
    chute = input("Qual letra?")
    return chute.strip().upper()

def marca_chute_correto(chute, palavra_secreta, letras_acertadas):
    index = 0
    for letra in palavra_secreta:
        if(chute == letra):
            letras_acertadas[index] = letra
        index += 1

